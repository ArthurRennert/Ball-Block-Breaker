package gui;

/**
 * The ScreenSettings class holds the global constants related to the game's display resolution.
 * These values define the dimensions of the game frame (screen width and height).
 */
public class ScreenSettings {

   /**
    * The width of the game frame in pixels.
    */
   public static final int FRAME_WIDTH = 1500;

   /**
    * The height of the game frame in pixels.
    */
   public static final int FRAME_HEIGHT = 900;
}
